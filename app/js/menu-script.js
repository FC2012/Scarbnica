function mOver(obj) {
    obj.style.background = "rgba(255,255,255,0.9)";
    obj.firstElementChild.style = "color:rgb(217, 170, 32)";
}

function mOut(obj) {
    obj.style.background = "rgba(51,1,2,0.8)";
    obj.firstElementChild.style = "color:rgba(255,255,255,0.9)";
}


